#!/bin/bash

# Скрипт для убийства процессов на портах разработки (3000 и 5000)
# Используется перед запуском npm start

echo "🚀 Подготовка к запуску разработки..."
echo "🔍 Проверка занятых портов..."

# Убиваем процессы на порту 3000 (React)
echo "📱 Проверка порта 3000 (Frontend)..."
PIDS_3000=$(lsof -ti:3000)
if [ ! -z "$PIDS_3000" ]; then
    echo "💀 Завершение процессов на порту 3000..."
    kill -9 $PIDS_3000
    echo "✅ Порт 3000 освобожден"
else
    echo "✅ Порт 3000 свободен"
fi

# Убиваем процессы на порту 5000 (Backend)
echo "🔧 Проверка порта 5000 (Backend)..."
PIDS_5000=$(lsof -ti:5000)
if [ ! -z "$PIDS_5000" ]; then
    echo "💀 Завершение процессов на порту 5000..."
    kill -9 $PIDS_5000
    echo "✅ Порт 5000 освобожден"
else
    echo "✅ Порт 5000 свободен"
fi

# Дополнительно убиваем процессы MongoDB если они есть
echo "🗄️  Проверка MongoDB процессов..."
MONGO_PIDS=$(pgrep -f "mongod")
if [ ! -z "$MONGO_PIDS" ]; then
    echo "💀 Завершение процессов MongoDB..."
    kill -9 $MONGO_PIDS
    echo "✅ MongoDB процессы завершены"
else
    echo "✅ MongoDB процессы не найдены"
fi

echo "🎉 Готово! Порты освобождены для разработки"
